package main;
import "fmt";

func main(){
	arr :=[8]string{"This","is","a","Go","interview","question","of","you"};
	slice :=arr[1:4];
	fmt.Println(slice);
	fmt.Println("capacity ",cap(slice));
	fmt.Println("Length ",len(slice));
	slice2 :=slice[1:2];
	fmt.Println("capacity ",cap(slice2));
	fmt.Println("Length ",len(slice2));


}

// Explanation

// Step-1:

// কোড সেগমেন্ট এ থাকবে main() মেথড , fmt.Println() মেথড , cap() মেথড, len() মেথড

// Step-2:

// প্রথমে স্ট্যাক এর ভিতর main ফ্রেম তৈরি হবে তার ভিতর থাকবে arr variable ৮ লেন্থ বিশিষ্ট ওই অ্যারে ৮ টা সেল নিয়ে থাকবে

// Step-3:

// স্লাইস নামক একটা ভ্যারিয়েবল তৈরি হবে যার ভিতর থাকবে পয়েন্টার অ্যাড্রেস যেটা যাবে অ্যার নামক অ্যারে র সেকেন্ড এলিমেন্টটার অ্যাড্রেস। কারণ এখানে ইনডেক্স ১:৪ দেয়া মানে ওই অ্যারে সেকেন্ড ইনডেক্স ১ থেকে ৫থ ইনডেক্স ৪ এর আগের ইনডেক্স পর্যন্ত মানে ইনডেক্স ৩ পর্যন্ত নিবে। স্লাইস এর লেন্থ হবে ৩, ৪-১=৩। ক্যাপাসিটি হবে এখানে যেহেতু ইনডেক্স ১ থেকে আনবে তাই ইনডেক্স ১ থেকে অ্যারে র লেন্থ পর্যন্ত এখানে অ্যারে তে হাইয়েস্ট ৮ টা এলিমেন্ট আছে তাহলে স্লাইস এর ক্যাপাসিটি হবে ৮-১=৭।

// Step-4: fmt.Println ফ্রেম তৈরি হবে স্ট্যাক এ এখানে স্লাইস কে প্রিন্ট করবে ওই স্লাইস যে সেল এ আছে ওই সেল এ আছে পয়েন্টার অ্যাড্রেস, লেন্থ=৩ আর ক্যাপাসিটি=৭।

// স্লাইস এর ভিতরে থাকে একটি পয়েন্টার অ্যাড্রেস (arr[1] এর অ্যাড্রেস), লেন্থ=৩ এবং ক্যাপাসিটি=৭। যেহেতু লেন্থ ৩ তাই স্লাইস arr থেকে ৩টা এলিমেন্ট নিবে।

// এখন এই ৩টা এলিমেন্ট বের হবে এইভাবে:

// ধরি পয়েন্টার অ্যাড্রেস = ১০০০;

// এখানে স্লাইস এর index শুরু হয় 0 থেকে।

// এখন element access হবে এইভাবে:

// ১ম এলিমেন্ট (index 0):

// ১০০০ + (০ × size_of_byte) = ১০০০ → "is"

// ২য় এলিমেন্ট (index 1):

// ১০০০ + (১ × size_of_byte) = ১০১৬ → "a"

// ৩য় এলিমেন্ট (index 2):

// ১০০০ + (২ × size_of_byte) = ১০৩২ → "Go"

// তাই Go runtime আসলে যেটা করে:

// pointer Addess + (index × size_of_byte) এইভাবেই slice এর address থেকে বাকি slice item গুলোর value বের করে। 64 bit computer এ size_of_byte হবে 16 মানে 16 byte হবে।

// মানে এখানে:
// size_of_element = 16 byte (string এর জন্য 64-bit system এ)
// index = 0,1,2

// Final output:
// ["is","a","Go"]

// Step-5:

// fmt.Println ফ্রেম তৈরি হবে এরপর cap ফ্রেম তৈরি হবে স্লাইস যে সেল এ স্টোর আছে মানে যে সেল এ পয়েন্টার অ্যাড্রেস , লেন্থ আর ক্যাপাসিটি আছে ওইখান থেকে ক্যাপাসিটি টা নিয়ে আমি ক্যাপাসিটি প্রিন্ট করবো এরপর ক্যাপ ফ্রেম পপ হবে , এরপর fmt.Println ফ্রেম পপ হবে

// Step-6:

// fmt.Println ফ্রেম তৈরি হবে এরপর লেন ফ্রেম তৈরি হবে স্লাইস যে সেল এ স্টোর আছে মানে যে সেল এ পয়েন্টার অ্যাড্রেস , লেন্থ আর ক্যাপাসিটি আছে ওইখান থেকে লেন্থ টা নিয়ে আমি লেন্থ প্রিন্ট করবো এরপর len ফ্রেম পপ হবে , এরপর fmt.Println ফ্রেম পপ হবে

// Step-7:

// স্লাইস২ := স্লাইস[১:২]; এইটার জন্য একটা সেল তৈরি হবে র‍্যাম এ তার ভিতর পয়েন্টার অ্যাড্রেস থাকবে যেটা হবে অ্যার নামক অ্যারে র ইনডেক্স ২ এর অ্যাড্রেস। মানে সরাসরি ওই অ্যারে র থার্ড ইনডেক্স এর সেল অ্যাড্রেস টা আনবে। লেন্থ হবে ২-১=১ , ক্যাপাসিটি হবে যেহেতু ইনডেক্স ২ থেকে আনবে তাই ক্যাপাসিটি হবে ৮-২=৬

// Step-8:

// এরপর সিমিলার ভাবে স্লাইস২ র জন্য fmt.Println ফ্রেম তৈরি হবে এরপর cap ফ্রেম তৈরি হবে স্লাইস২ থেকে ক্যাপাসিটি নিয়ে আসবে। তারপর cap ফ্রেম পপ হবে , তারপর fmt.Println ফ্রেম পপ হবে

// Step-9:

// এরপর সিমিলার ভাবে স্লাইস২ র জন্য fmt.Println ফ্রেম তৈরি হবে এরপর len ফ্রেম তৈরি হবে স্লাইস২ থেকে লেন্থ নিয়ে আসবে। তারপর len ফ্রেম পপ হবে , তারপর fmt.Println ফ্রেম পপ হবে